                                    Construcción de Carreteras
                                </h4>
                                <p>
                                    El icopor reemplaza el uso de materiales de relleno pesados evitando el exceso de cargas sobre el terreno o las estructuras adyacentes y disminuyendo la cantidad de compactación.
                                </p>
                            </div> 

                            <div class="col-md-12" style="margin-bottom:50px;"><hr>
                                <h4>Construcción de Puentes</h4>
                                <p>Se utiliza en el relleno de los pilares soportando sobrecargas de tráfico sin alterar el terreno mejorando los costos de construcción y tiempos de mantenimiento.</p>
                            </div> 

                           <div class="col-md-12" style="margin-bottom:50px;"><hr>
                                <h4>Construcción de estructuras de ferrocarril</h4>
                                <p>El icopor por su resistencia mecánica se utiliza para evitar sobrecargas en el terreno haciendo más eficientes los tiempos de construcción.</p>
                            </div>                             

                       
<hr>
                        <div class="col-md-12" style="margin-bottom:50px;"><hr>
                            
                                <h4>Muros de contención</h4>
                                <p> Cuando se realizan elementos verticales de contención el icopor se comporta como un relleno portante, reduciendo las cargas laterales sobre la estructura debido a que la presión horizontal que actúa sobre el muro de contención es proporcional al peso del relleno.</p>
                            </div>

                            <div class="col-md-12" style="margin-bottom:50px;"><hr>
                                <h4>Elevación para barreras de ruido</h4>
                                <p>El icopor reemplaza parte de los terrenos existentes disminuyendo el peso de los taludes y mejorando su estabilidad.</p>
                            </div>

                            <div class="col-md-12" style="margin-bottom:50px;"><hr>
                                <h4>Estructuras flotantes</h4>
                                <p>
                                    Al realizar paredes elevadas que actúan como barrera acústica por ejemplo (vías de ferrocarril, pistas de aterrizaje, sitios con altos estándares de ruido) el icopor reduce la transmisión de vibraciones del terreno y neutraliza las ondas, encapsulando y disminuyendo los niveles de ruido.
                                </p>
                            </div>

                        
<hr>
                     <div class="col-md-12" style="margin-bottom:50px;"><hr>
                             
                                <h4>Estabilidad de taludes</h4>
                                <p>El icopor se utiliza en la construcción  de cimentaciones flotantes, aprovechando su resistencia al agua, durabilidad y flotabilidad ayudando a repartir las cargas en todos los puntos de la estructura y permitiendo su desplazamiento con la variación del nivel del agua.</p>
                            </div>
                           <div class="col-md-12" style="margin-bottom:50px;"><hr>
                                <h4>Construcción de diques</h4>
                                <p>Por su bajo peso y gran volumen el icopor ayuda en la construcción de diques disminuyendo el peso de los rellenos de tierra tradicional, cargas adicionales, ciclos de asentamiento y elevación posterior de los diques.</p>
